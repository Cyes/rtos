void LCD_WR_REG(uint8_t dat)
{
	SET_DC_PIN(0);
	spix_readwrite(&dat,1);
	SET_DC_PIN(1);
}


uint16_t bswap_16(uint16_t a) 
{
	return ((a >> 8) & 0xFF) | ((a << 8) & 0xFF00);
} 



void	LCD_WR_DATA(uint16_t dat)
{
	dat = bswap_16(dat);
	spix_readwrite(&dat,2);
}


void LCD_WR_DATA8(uint8_t dat)
{
	spix_readwrite(&dat,1);
}




void lcd_window(uint16_t x1,uint16_t y1,uint16_t x2,uint16_t y2)
{
		LCD_WR_REG(0x2a);
		LCD_WR_DATA(x1+40);
		LCD_WR_DATA(x2+40);
		LCD_WR_REG(0x2b);
		LCD_WR_DATA(y1+53);
		LCD_WR_DATA(y2+53);
		LCD_WR_REG(0x2c);
}


void lcd_fill(uint16_t xsta,uint16_t ysta,uint16_t xend,uint16_t yend,uint16_t color)
{          
	uint16_t i,j; 
	lcd_window(xsta,ysta,xend-1,yend-1); 
	
	for(i=ysta;i<yend;i++){													   	 	
		for(j=xsta;j<xend;j++){
			LCD_WR_DATA(color);
		}
	} 					  	    
}



void lcd_drawchar(uint16_t x,uint16_t y,uint8_t num,uint16_t fc,uint16_t bc)
{
	uint8_t t,m=0;
	num=num-' ';
	lcd_window(x,y,x+8-1,y+16-1);
	for(int i=0;i<16;i++)
	{ 
		for(int t=0;t<8;t++)
		{
				if(ascii_1608[num][i]&(0x01<<t))LCD_WR_DATA(fc);
				else LCD_WR_DATA(bc);
				m++;
				if(m%8==0){
					m=0;
					break;
				}			
		}
	}   	 	  
}


